CREATE OR REPLACE FUNCTION "public"."match_artist_comparisons"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer) RETURNS TABLE("artist1_id" "uuid", "artist2_id" "uuid", "comparison_text" "text", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    SELECT
        ac.artist1_id,
        ac.artist2_id,
        ac.comparison_text,
        1 - (ac.embedding <=> query_embedding) as similarity
    FROM artist_comparisons ac
    WHERE 1 - (ac.embedding <=> query_embedding) > match_threshold
    ORDER BY similarity DESC
    LIMIT match_count;
END;
$$;


